package casting;

public class ExplicitCasting {

	public static void main(String[] args) {
		
		//Data Type---->Higher to Lower
		//Memory--->Decreased----->Narrowing Casting
	    //Data Loss--->Data Loss Take place
		
		double b=100.123d;
		int a=(int)b;
		
		System.out.println(b);
		System.out.println(a);

	}

}
