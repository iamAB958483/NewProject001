package casting;

public class ImplicitCasting {

	public static void main(String[] args) {
		
		//Data Type---->Lower to Higher
		//Memory--->Increased----->Widening Casting
		//Data Loss--->No Data Loss
		
		int a=10;
		long b=a;
		
		
		short p=123;
		double q=p;
		System.out.println(p);
        System.out.println(q);
	}

}
