package controlStatementStudy;

public class HomeWork5 {

	public static void main(String[] args) {
		
		String Days="Friday";
		
		switch(Days)
		{
		case "Monday": System.out.println("1st Day Of Week is");
		break;
		
		case "Tuesday": System.out.println("2nd Day Of Week is");
		break;
		
		case "Wednesday": System.out.println("3rd Day Of Week is");
		break;
		
		case "Thursday": System.out.println("4th Day Of Week is");
		break;
		
		case "Friday": System.out.println("5th Day Of Week is");
		break;
		
		case "Saturday": System.out.println("6th Day Of Week is");
		break;
		
		case "Sunday": System.out.println("7th Day Of Week is");
		break;
		
		default:System.out.println("Invalid Day");
		break;
		}
	   			
	}

}
